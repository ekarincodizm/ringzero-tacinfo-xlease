<h1>ประกาศผลการแข่งขันโครงการ TADC 2013</h1>
    <p id="date">17/01/2014</p>
    <p id="detail">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;รางวัลชนะเลิศ ได้แก่ <b>ทีม PlaSooK</b> ได้รับเงินรางวัลมูลค่า 365,000 บาท<br>
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;รางวัลรองชนะเลิศอันดับที่ 1 ได้แก่ ทีม Seed และ ทีม Mo-ring Monday ได้รับเงินรางวัลมูลค่าทีมละ 12,500 บาท<br><br>

<b>หมายเหตุ :</b><br>
1. เนื่องจากคะแนนเท่ากัน จึงมีทีมที่ได้รางวัล รองชนะเลิศอันดับที่ 1 จำนวน 2 ทีม<br>
2. วันที่รับรางวัลคือวันที่ 26 มกราคม 2557 ที่ บริษัท แทค อินโฟ จำกัด เวลา 14.00 น.<br>
3. ให้ผู้เข้าแข่งขันทุกท่านที่ได้รับรางวัล นำ สำเนาบัตรประชาชน และ สำเนาทะเบียนบ้าน ท่านละ 1 ชุด <br>&nbsp;&nbsp; &nbsp;มามอบให้บริษัทฯในวันที่ 26 มกราคม 2557 ด้วยเพื่อใช้เป็นเอกสารประกอบในการรับเงินรางวัล<br>
4. หากมีข้อสงสัยประการใด กรุณาติดต่อ 02-744-2388 หรือ 02-744-2222 ต่อ 5415 , 5416
	</p><br>
